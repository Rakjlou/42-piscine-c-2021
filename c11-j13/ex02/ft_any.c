int	ft_any(char **tab, int(*f)(char*))
{
	int	i;
	int	flag;

	i = 0;
	flag = 0;
	while (tab[i])
	{
		if (f(tab[i]) != 0)
			return (1);
		i++;
	}
	return (0);
}
